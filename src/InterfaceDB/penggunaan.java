public class penggunaan {
        private String bulan;
        private String tahun;
        private String meter_awal;
        private String meter_akhir;

    public String getBulan() {
        return bulan;
    }

    public void setBulan(String bulan) {
        this.bulan = bulan;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public String getMeter_awal() {
        return meter_awal;
    }

    public void setMeter_awal(String meter_awal) {
        this.meter_awal = meter_awal;
    }

    public String getMeter_akhir() {
        return meter_akhir;
    }

    public void setMeter_akhir(String meter_akhir) {
        this.meter_akhir = meter_akhir;
    }

        
}
